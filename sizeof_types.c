#include <stdio.h>

int main() {
    printf("sizeof(char)      == %d\n",  sizeof(char));
    printf("sizeof(short)     == %d\n",  sizeof(short));
    printf("sizeof(int)       == %d\n",  sizeof(int));
    printf("sizeof(long)      == %ld\n", sizeof(long));
    printf("sizeof(long long) == %ld\n", sizeof(long long));

    return 0;
}
